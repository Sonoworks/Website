function iirFilter(b, a, x) {
    // Direct Form II transposed IIR filter
    // Equivalent to MATLAB's filter(b, a, x)
    const y = new Float32Array(x.length);
    const nb = b.length;
    const na = a.length;
    const z = new Float32Array(Math.max(nb, na));

    for (let n = 0; n < x.length; n++) {
        y[n] = b[0] * x[n] + z[0];
        for (let k = 1; k < Math.max(nb, na); k++) {
            z[k - 1] = (k < nb ? b[k] * x[n] : 0)
                     + (k < na ? -a[k] * y[n] : 0)
                     + (k + 1 < Math.max(nb, na) ? z[k] : 0);
        }
    }
    return y;
}

function rms(signal) {
    let sum = 0;
    for (let i = 0; i < signal.length; i++) sum += signal[i] * signal[i];
    return Math.sqrt(sum / signal.length);
}

